     public void fillValue() {
		for(int i = 0;i < n;i = i + sqn) {
			fillBox(i,i);
			}
                
		fillRemaining(0,sqn);
		removeKDigits();		
		}

	boolean unUsedInBox(int rowstart,int colstart,int num) {		//rreturn false if given 3*3 box contains num
		for(int i = 0;i<sqn;i++) {
			for(int j = 0;j<sqn;j++) {
				if(mat[rowstart + i][colstart + j] == num) {
					return false;
					}
				}
			}
		return true;
		}

	void fillBox(int row,int col) {
		int num;
		for(int i = 0;i<sqn;i++){
			for(int j = 0;j<sqn;j++) {
				do {
					num = (int) Math.floor((Math.random()*n+1));
				}
				while(!unUsedInBox(row,col,num));
				mat[row + i][col + j] = num;
				}
			}
		}

	boolean checkIfSafe(int r,int c,int num) {
		for(int k = 0;k < n;k++) {
			if(mat[r][k] == num) {
				return false;
				}
			if(mat[k][c] == num) {
				return false;
				}
		}
		int rowstart = r - r%sqn;
		int colstart = c - c%sqn;
		for(int i = 0;i<sqn;i++) {
			for(int j = 0;j<sqn;j++) {
				if(mat[rowstart + i][colstart + j] == num) {
					return false;
					}
				}
			}
			
		return true;
		}
	
	boolean fillRemaining(int r,int c) {
		if (c>=n && r<n-1) { // j>=9 && i < 8
            r = r + 1; 
            c = 0; 
        } 
        if (r>=n && c>=n) // i >= 9 && j >= 9
            return true; 
  
        if (r < sqn) { 	// i < 3 it is to skip the first diagonal 3x3 matrix
            if (c < sqn) // j< 3
                c = sqn; // j = 3
        } 
        else if (r < n-sqn)  { 	// i < 6  it is to skip the second diagonal 3x3
            if (c==(int)(r/sqn)*sqn) // j == 3
                c =  c + sqn; 	//  j =  6
        } 
        else { 
            if (c == n-sqn)  { 	// j ==  6 && i < 9
                r = r + 1; 
                c = 0; 
                if (r>=n) 
                    return true; 
            } 
        } 
  
        for (int num = 1; num<=n; num++)  { 
            if (checkIfSafe(r, c, num))   { 
                mat[r][c] = num; 
                if (fillRemaining(r, c+1)) { 
                    return true;  }
                mat[r][c] = 0; 
            } 
        } 
        return false; 
     } 

	private void removeKDigits() {
		int count = k;
		while(count != 0) {
			int cellID = (int) Math.floor((Math.random()*(n*n)+1));
			int i = (cellID/n); 
           	int j = cellID%n; 
         	if(i != n){
         		if (mat[i][j] != 0) {
           	     count--; 
           		 mat[i][j] = 0; 
          		  }
          	 } 
	 	 }
	}
