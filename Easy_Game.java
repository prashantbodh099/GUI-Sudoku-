    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       JTextField temp  = null;
        int[][] solved = new int[n][n];
        int i = 0,j = 0;
        boolean check = false;
        Border border = BorderFactory.createLineBorder(Color.red, 3);
        
        
        for(Component c:jPanel2.getComponents()){
                if(c.getClass().toString().contains("javax.swing.JTextField")){
                      temp=(JTextField)c;
                      if((temp.getText().length() > 1)||(temp.getText().isBlank())||(isNumeric(temp.getText()) == false)) {
                          temp.setBorder(border);
                          check = false;
                      }
                      else  {
                          temp.setBorder(null);
                          solved[i][j] = Integer.valueOf(temp.getText());
                       j++;
                      if(j == 6){
                          j = 0;
                          i++;
                      }
                      if( i == 6){
                          break;
                      } 
                      check = true;
                    }
                 }
            }
        int sum_r = 0,sum_c = 0,sum_s = 0;
        int m,l;
        boolean checkr = false,checkc = false,checks = false;
        if(check == true){
            for(i = 0;i < n;i++){
                sum_r = 0;
                sum_c = 0;
                for(j = 0;j < n;j++){
                    sum_r = sum_r + solved[i][j];
                    sum_c = sum_c + solved[j][i];
                }
                checkr = sum_r == 21; //   break;
                checkc = sum_c == 21;
            }
           for(i = 0;i<n;i = i+3){
               for(j = 0;j<n ;j = j+2){
                   for(m = i;m < (i+3);m++){
                       for(l = j;m < (j+2);m++){
                           sum_s = sum_s + solved[m][l];
                       }
                   }
                   checks = sum_s == 21; //  break;
                   sum_s = 0;
               }
           } 
          if(checkr && checkc && checkc) {
              jLabel1.setText("Congratulations " + pop);
          }
          else{
              jLabel1.setText(pop + ",Try again");
          }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

